// Regenerate js/env-assets.js — embeds sky HDR + terrain textures as data URIs
// so the game works from file:// (fetch/XHR blocked there).
const fs = require('fs');
const jpg = p => 'data:image/jpeg;base64,' + fs.readFileSync(p).toString('base64');
const out = '// GENERATED by tools/build-env-assets.js — do not edit\n' +
  'const ASSET_GRASS_JPG = "' + jpg('assets/tex/grass.jpg') + '";\n' +
  'const ASSET_ROCK_JPG  = "' + jpg('assets/tex/rock.jpg') + '";\n' +
  'const ASSET_SAND_JPG  = "' + jpg('assets/tex/sand.jpg') + '";\n' +
  'const ASSET_SKY_HDR   = "data:application/octet-stream;base64,' +
  fs.readFileSync('assets/golden_bay_1k.hdr').toString('base64') + '";\n';
fs.writeFileSync('js/env-assets.js', out);
console.log('wrote js/env-assets.js', Math.round(out.length / 1024) + ' KB');
